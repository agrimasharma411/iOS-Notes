//What is ARC ?

/*
 
1.  ARC stands for Automatic Reference Counting.
2.  It’s Swift’s memory management system.
3.  ARC automatically keeps track of how many references are pointing to an object.
4.  When no one needs the object anymore, ARC deallocates it automatically,
5.  And it free up memory.
6.  we don’t need to manually free memory
 
